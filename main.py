# --- 4. AI CORE INTELLIGENCE ---
def extract_pdf_text(filepath):
    if not filepath: return ""
    try:
        reader = PdfReader(filepath)
        text = ""
        for page in reader.pages: text += page.extract_text() + "\n"
        return text
    except Exception as e: return f"Error reading PDF: {str(e)}"

def career_coach_logic(license_key, resume_file, jd_file):
    # 1. Security Check
    is_valid, msg = verify_gumroad_key(license_key)
    if not is_valid:
        return f"üîí SECURITY ALERT: {msg}", None

    # 2. File Check
    if not resume_file or not jd_file:
        return "‚ö†Ô∏è Missing Files: Please upload BOTH your Resume and the Job Description.", None

    # 3. Processing
    res_text = extract_pdf_text(resume_file)
    jd_text = extract_pdf_text(jd_file)

    prompt = f"""
    ROLE: You are an Executive Career Strategist (Human Capital Expert).
    TASK: Analyze the Candidate's Resume against the Target Job Description (JD).
    INPUTS: 
    - RESUME: {res_text} 
    - JD: {jd_text}
    
    OUTPUT FORMAT: Strict Markdown.
    
    # üèÅ PART 1: STRATEGIC FIT ANALYSIS
    ## üö¶ The Verdict
    * **Status:** [STRONG MATCH / POSSIBLE / WEAK]
    * **Fit Score:** [0-100]
    * **Executive Summary:** (2 sentences regarding the decision)
    
    ## üö© Critical Gaps & Risks
    * (Bullet point 1)
    * (Bullet point 2)
    * (Bullet point 3)
    
    ---
    
    # ‚úçÔ∏è PART 2: THE REWRITE
    ## üíé Professional Summary (Optimized)
    (Write a compelling, metric-heavy bio)
    
    ## üõ†Ô∏è Core Competencies (ATS Keywords)
    (Provide a 3x4 grid of skills present in the JD that the candidate possesses)
    
    ## üöÄ Experience (Top 2 Roles Rewritten)
    **(Latest Role Title)**
    * (Action Verb) + (Context) + (Result/Metric)
    * (Action Verb) + (Context) + (Result/Metric)
    * (Action Verb) + (Context) + (Result/Metric)
    
    **(Previous Role Title)**
    * (Action Verb) + (Context) + (Result/Metric)
    * (Action Verb) + (Context) + (Result/Metric)
    
    ---
    
    # ‚úâÔ∏è PART 3: OUTREACH
    ## üéØ Recruiter Hook (Email Opener)
    (A 3-sentence powerful opening paragraph for a cover letter or LinkedIn message)
    """
    
    try:
        response = model.generate_content(prompt)
        ai_text = response.text
        pdf_path = create_pdf(ai_text)
        return ai_text, pdf_path
    except Exception as e:
        return f"System Error: {e}", None

